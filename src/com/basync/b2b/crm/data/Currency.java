package com.basync.b2b.crm.data;

public class Currency {

	private String currAbrev;
	private String currency;
	private String currSymbol;
	private String country;
	private String hundredsName;
	private int autoUpdate;
	private int status;
	/**
	 * @return the currAbrev
	 */
	public String getCurrAbrev() {
		return currAbrev;
	}
	/**
	 * @param currAbrev the currAbrev to set
	 */
	public void setCurrAbrev(String currAbrev) {
		this.currAbrev = currAbrev;
	}
	/**
	 * @return the currency
	 */
	public String getCurrency() {
		return currency;
	}
	/**
	 * @param currency the currency to set
	 */
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	/**
	 * @return the currSymbol
	 */
	public String getCurrSymbol() {
		return currSymbol;
	}
	/**
	 * @param currSymbol the currSymbol to set
	 */
	public void setCurrSymbol(String currSymbol) {
		this.currSymbol = currSymbol;
	}
	/**
	 * @return the country
	 */
	public String getCountry() {
		return country;
	}
	/**
	 * @param country the country to set
	 */
	public void setCountry(String country) {
		this.country = country;
	}
	/**
	 * @return the hundredsName
	 */
	public String getHundredsName() {
		return hundredsName;
	}
	/**
	 * @param hundredsName the hundredsName to set
	 */
	public void setHundredsName(String hundredsName) {
		this.hundredsName = hundredsName;
	}
	/**
	 * @return the autoUpdate
	 */
	public int getAutoUpdate() {
		return autoUpdate;
	}
	/**
	 * @param autoUpdate the autoUpdate to set
	 */
	public void setAutoUpdate(int autoUpdate) {
		this.autoUpdate = autoUpdate;
	}
	/**
	 * @return the status
	 */
	public int getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(int status) {
		this.status = status;
	}

	
}
