package com.basync.b2b.crm.data;

import java.util.Date;

public class ProfitLossData {
	private String companyName;
	private String purchaseDate;
	private String dueDate;
	private double amount;
	private double expenses;
	private double weight;
	private double cts;
	private double finalRate;
	private String pktcode;
	private Integer purchaseId;
	/**
	 * @return the pktcode
	 */
	public String getPktcode() {
		return pktcode;
	}
	/**
	 * @param pktcode the pktcode to set
	 */
	public void setPktcode(String pktcode) {
		this.pktcode = pktcode;
	}
	
	/**
	 * @return the purchaseId
	 */
	public Integer getPurchaseId() {
		return purchaseId;
	}
	/**
	 * @param purchaseId the purchaseId to set
	 */
	public void setPurchaseId(Integer purchaseId) {
		this.purchaseId = purchaseId;
	}
	public String getCompanyName() {
		return companyName;
	}
	public String getPurchaseDate() {
		return purchaseDate;
	}
	public String getDueDate() {
		return dueDate;
	}
	public double getAmount() {
		return amount;
	}
	public double getExpenses() {
		return expenses;
	}
	public double getWeight() {
		return weight;
	}
	public double getCts() {
		return cts;
	}
	public double getFinalRate() {
		return finalRate;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public void setPurchaseDate(String purchaseDate) {
		this.purchaseDate = purchaseDate;
	}
	public void setDueDate(String dueDate) {
		this.dueDate = dueDate;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public void setExpenses(double expenses) {
		this.expenses = expenses;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
	public void setCts(double cts) {
		this.cts = cts;
	}
	public void setFinalRate(double finalRate) {
		this.finalRate = finalRate;
	}
}
