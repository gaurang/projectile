package com.basync.b2b.crm.data;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="tb_invoiceDetails")
public class InvoiceDetail implements Serializable {

	/**
	 * 
	 */
	
	private static final long serialVersionUID = -8466279690810171545L;
	
	@Id
	@Column(name="invId", insertable = false, updatable = false)
	 private Integer invId ;
	
	@Id
	@Column(name="pktId")
	 private Integer pktId ;
	
	@Column(name="rate")
	 private Double rate; 
	
	@Column(name="totalRate")
	 private Double	totalRate ;
	
	@Column(name="tax")
	 private Double	tax;
	
	@Column(name="discount")
	 private Double	discount;
	
	@Column(name="finalRate")
	 private Double	finalRate ;
	
	@Column(name="status")
	 private Integer status;
	
	@Column(name="returnDate")
	 private String returnDate ;
	
	@Column(name="rejCts")
	 private String rejCts ;

	
	/**
	 * @return the invId
	 */
	public Integer getInvId() {
		return invId;
	}
	/**
	 * @param invId the invId to set
	 */
	public void setInvId(Integer invId) {
		this.invId = invId;
	}
	/**
	 * @return the pktId
	 */
	public Integer getPktId() {
		return pktId;
	}
	/**
	 * @param pktId the pktId to set
	 */
	public void setPktId(Integer pktId) {
		this.pktId = pktId;
	}
	/**
	 * @return the rate
	 */
	public Double getRate() {
		return rate;
	}
	/**
	 * @param rate the rate to set
	 */
	public void setRate(Double rate) {
		this.rate = rate;
	}
	/**
	 * @return the totalRate
	 */
	public Double getTotalRate() {
		return totalRate;
	}
	/**
	 * @param totalRate the totalRate to set
	 */
	public void setTotalRate(Double totalRate) {
		this.totalRate = totalRate;
	}
	/**
	 * @return the tax
	 */
	public Double getTax() {
		return tax;
	}
	/**
	 * @param tax the tax to set
	 */
	public void setTax(Double tax) {
		this.tax = tax;
	}
	/**
	 * @return the discount
	 */
	public Double getDiscount() {
		return discount;
	}
	/**
	 * @param discount the discount to set
	 */
	public void setDiscount(Double discount) {
		this.discount = discount;
	}
	/**
	 * @return the finalRate
	 */
	public Double getFinalRate() {
		return finalRate;
	}
	/**
	 * @param finalRate the finalRate to set
	 */
	public void setFinalRate(Double finalRate) {
		this.finalRate = finalRate;
	}
	/**
	 * @return the status
	 */
	public Integer getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(Integer status) {
		this.status = status;
	}
	/**
	 * @return the returnDate
	 */
	public String getReturnDate() {
		return returnDate;
	}
	/**
	 * @param returnDate the returnDate to set
	 */
	public void setReturnDate(String returnDate) {
		this.returnDate = returnDate;
	}
	/**
	 * @return the rejCts
	 */
	public String getRejCts() {
		return rejCts;
	}
	/**
	 * @param rejCts the rejCts to set
	 */
	public void setRejCts(String rejCts) {
		this.rejCts = rejCts;
	}
	
	
}
